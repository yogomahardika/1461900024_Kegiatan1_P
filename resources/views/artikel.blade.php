    <h2>Apa itu framework?</h2>
    <p> Sesuai dengan namanya sendiri, framework adalah kerangka kerja untuk mengembangkan aplikasi berbasis website maupun desktop. Kerangka kerja disini sangat membantu developer dalam menuliskan sebuah dengan lebih terstruktur dan tersusun rapi.
Kerangka kerja diciptakan untuk mempermudah kinerja dari programmer. Sehingga, seorang programmer tidak perlu untuk menuliskan kode secara berulang – ulang. Karena di dalamnya sendiri anda hanya perlu menyusun komponen – komponen pemrograman saja.
Framework sengaja diciptakan untuk membantu developer mengembangkan aplikasi atau website lebih cepat serta tersusun dan terstruktur. 
Dengan begitu, developer dan programmer tidak perlu melakukan koding program yang diulang-ulang.
<p>Fungsi Framework</p>
<p>Framework memiliki fungsi utama untuk mempermudah para developer mengembangkan aplikasi dan website terkait struktur MVC (Model View Controller) yang digunakan. Selain itu, ada tiga fungsi framework yang dapat Anda ketahui di bawah ini:</p>
<p>1. Program Menjadi Lebih Terstruktur dan Tersusun</p>
<p>2. Praktis untuk Developer</p>
<p>3. Memiliki Keamanan yang Lebih Unggul </p>
